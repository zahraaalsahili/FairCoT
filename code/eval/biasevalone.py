def convert_webp_to_jpg(folder_path):
    # Loop through all files in the folder
    for filename in os.listdir(folder_path):
        if filename.endswith(".webp"):
            webp_path = os.path.join(folder_path, filename)
            # Change the file extension to .jpg
            jpg_filename = filename.rsplit(".", 1)[0] + ".jpg"
            jpg_path = os.path.join(folder_path, jpg_filename)
            
            # Open the .webp image
            with Image.open(webp_path) as img:
                # Convert the image to RGB (some webp images have transparency)
                img = img.convert("RGB")
                # Save it as a .jpg image
                img.save(jpg_path, "JPEG")
                print(f"Converted {webp_path} to {jpg_path}")
            
            # Delete the original .webp file
            os.remove(webp_path)
            print(f"Deleted original .webp file: {webp_path}")



