# Function to load images from a folder
def load_images_from_folder(folder_path):
    image_paths = []
    for filename in os.listdir(folder_path):
        if filename.endswith(".png") or filename.endswith(".jpg") or filename.endswith(".jpeg"):
            image_path = os.path.join(folder_path, filename)
            image_paths.append(image_path)
    images = [Image.open(img_path) for img_path in image_paths]
    return images, image_paths

# Iterate over each job in test_classes
for job in test_classes:
    # Set paths and folder for each job
    ppath = f'/data/home/ours-multi-v2/{job}'
    
    # Load images for the current job
    images, image_paths = load_images_from_folder(ppath)
    
    # Initialize a list to store cropped faces
    sub_images = []

    # Process each image file
    for img_file in image_paths:
        # Read the image
        img = cv2.imread(img_file)
        if img is None:
            print(f"Could not read image '{img_file}'. Skipping.")
            continue
        
        # Convert to grayscale for face detection
        gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
        
        # Load the Haar cascade classifier
        face_cascade = cv2.CascadeClassifier(cv2.data.haarcascades + 'haarcascade_frontalface_default.xml')
        
        # Detect faces
        faces = face_cascade.detectMultiScale(gray, scaleFactor=1.1, minNeighbors=5, minSize=(30, 30))
        
        # If faces are not exactly 3, continue to the next image
        if len(faces) != 3:
            print(f"Expected 3 faces in '{img_file}', but found {len(faces)}.")
            continue
        
        # Crop and store each detected face
        for (x, y, w, h) in faces:
            sub_image = img[y:y+h, x:x+w]
            sub_images.append(sub_image)
    
    # Create a directory to store the cropped faces for this job
    folder_path = f"/data/home/ours-multi-v2/subl/{job}/"
    if not os.path.exists(folder_path):
        os.makedirs(folder_path)
        print(f"Folder created: {folder_path}")
    
    # Save each cropped face
    for idx, face_img in enumerate(sub_images):
        cv2.imwrite(f"{folder_path}/cropped_face_{idx}.jpg", face_img)
        print(f"Saved cropped face {idx} for {job}")


