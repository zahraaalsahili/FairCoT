  def zero_shot_classification(image_path, labels, model, preprocess, device):
      # Load and preprocess the image
      image = Image.open(image_path)
      image = preprocess(image).unsqueeze(0).to(device)

      # Prepare the texts
      texts = [clip.tokenize(f"a photo of {label}") for label in labels]
      texts = torch.cat(texts).to(device)

      # Calculate features
      with torch.no_grad():
          image_features = model.encode_image(image)
          text_features = model.encode_text(texts)

      # Calculate cosine similarity and find the best labels
      image_features /= image_features.norm(dim=-1, keepdim=True)
      text_features /= text_features.norm(dim=-1, keepdim=True)
      similarity = (100.0 * image_features @ text_features.T).softmax(dim=-1)
      scores = similarity[0].cpu().numpy()

      # Find the label with the highest score
      max_score_idx = scores.argmax()
      best_label = labels[max_score_idx]
      best_score = scores[max_score_idx]

      return best_label, best_score

